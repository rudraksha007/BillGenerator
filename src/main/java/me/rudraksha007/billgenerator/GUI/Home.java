    private void btnSalerDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalerDataActionPerformed
        if (txtCompany.isEditable()){
            if (txtCompany.getText().isEmpty()||txtAddress.getText().isEmpty()||txtPhn.getText().isEmpty()||
                    txtGSTIN.getText().isEmpty()||txtBank.getText().isEmpty()||txtAcc.getText().isEmpty()||
                    txtBranch.getText().isEmpty() ||txtIfsc.getText().isEmpty()||txtCgst.getText().isEmpty()||
                    txtSgst.getText().isEmpty()||txtIgst.getText().isEmpty())return;
            btnSalerData.setText("Edit User Data");
            Main.data = new UserData(txtCompany.getText(), txtAddress.getText(),
                    String.valueOf(cmbState.getSelectedItem()), txtPhn.getText(), txtGSTIN.getText(),
                    txtBank.getText(),txtAcc.getText(),txtBranch.getText(), txtIfsc.getText(), Float.parseFloat(txtCgst.getText()),
                    Float.parseFloat(txtSgst.getText()), Float.parseFloat(txtIgst.getText()));
            btnCancelActionPerformed(null);
            new DataManager().saveData(Main.data, "userData.data");
        }else {
            toggleAccessibility();
        }
    }//GEN-LAST:event_btnSalerDataActionPerformed

