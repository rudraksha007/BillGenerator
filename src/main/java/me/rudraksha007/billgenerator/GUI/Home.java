    public void toggleAccessibility(){
        if (txtCompany.isEditable()){
            for (Component c: pnlUserData.getComponents()){
                if (c instanceof JTextField) ((JTextField) c).setEditable(false);
                else c.setEnabled(false);
            }
            btnSalerData.setText("Save");
            for (Component c: pnlButtons.getComponents()){
                c.setEnabled(true);
            }
            btnCancel.setVisible(false);
            btnSalerData.setText("Edit User Data");
            pnlBank.setVisible(false);
            pnlLabels.setVisible(false);
        }
        else {
            btnSalerData.setText("Save");
            for (Component c: pnlButtons.getComponents()){
                c.setEnabled(c.equals(btnCancel) || c.equals(btnSalerData));
            }
            pnlBank.setVisible(true);
            pnlLabels.setVisible(true);
            btnCancel.setVisible(true);
            for (Component c: pnlUserData.getComponents()){
                if (c instanceof JTextField) ((JTextField) c).setEditable(true);
                else c.setEnabled(true);
            }
        }
    }
    public void setData(){
        UserData data = Main.data;
        txtCompany.setText(data.getCompanyName());
        txtAddress.setText(data.getAddress());
        cmbState.setSelectedItem(data.getState());
        txtPhn.setText(data.getPhn());
        txtGSTIN.setText(data.getGstin());
        txtBank.setText(data.getBankName()); ;
        txtAcc.setText(data.getAccNo());
        txtBranch.setText(data.getBranch());
        txtIfsc.setText(data.getIfsc());
        txtCgst.setText(String.valueOf(data.getCgst()));
        txtSgst.setText(String.valueOf(data.getSgst()));
        txtIgst.setText(String.valueOf(data.getIgst()));
    }
